    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:

        Produto obj = new Produto();
        obj.setCodigo(Integer.parseInt(codigoCadastro.getText()));
        obj.setProduto(nomeProdutoCadastro.getText());
        obj.setValorCompra(Double.parseDouble(valorCompraCadastro.getText().replace(',', '.')));
        obj.setValorVenda(Double.parseDouble(valorVendaCadastro.getText().replace(',', '.')));
        obj.setMarca(marcaCadastro.getSelectedItem().toString());
        obj.setQuantidade(Integer.parseInt(quantidadeCadastro.getText()));

        obj.setId(Integer.parseInt(idCadastro
                .getText()));

        ProdutoDao dao = new ProdutoDao();

        dao.editarProduto(obj);

        new utilitarios().LimpaTela(jPanel2);
    }//GEN-LAST:event_btnEditarActionPerformed

