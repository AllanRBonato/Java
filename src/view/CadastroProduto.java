    private void btnPesquisarEstoqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarEstoqueActionPerformed
        // TODO add your handling code here:

        String produto = "%" + nomeProdutoEstoque.getText() + "%";

        ProdutoDao dao = new ProdutoDao();
        List<Produto> listar = dao.buscarClientePorNome(produto);
        DefaultTableModel dados = (DefaultTableModel) tableEstoque.getModel();
        dados.setNumRows(0);

        for (Produto c : listar) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getCodigo(),
                c.getProduto(),
                c.getValorCompra(),
                c.getValorVenda(),
                c.getMarca(),
                c.getQuantidade()
            });
        }
    }//GEN-LAST:event_btnPesquisarEstoqueActionPerformed

